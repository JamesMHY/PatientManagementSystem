    private void Add_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Add_buttonActionPerformed
        // TODO add your handling code here:
        int qty = (Integer) Quantity_spinner.getValue();
        
        if (qty > 0 && !Dosage_text.getText().isEmpty() && Medicine_combobox.getSelectedIndex() != 0) {

            Gson gson = new GsonBuilder().create();

            try {
                stock_reader = Files.newBufferedReader(Paths.get("src//data//medicine_stock.json"));
            } catch (IOException ex) {
                Logger.getLogger(ConsultationScreen.class.getName()).log(Level.SEVERE, null, ex);
            }
            java.lang.reflect.Type stock_listType = new TypeToken<ArrayList<MedicineStock>>() {
            }.getType();
            ArrayList<MedicineStock> stocks = gson.fromJson(stock_reader, stock_listType);
            
            for (MedicineStock s : stocks) {
                if (s.getMedicine_name().equals(Medicine_combobox.getSelectedItem().toString())) {
                    if (s.getQuantity() < 10) {
                        JOptionPane.showMessageDialog(null, "The remain stock is less than 10.", "REMINDER", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        model.addRow(new Object[]{Medicine_combobox.getSelectedItem().toString(), qty, Dosage_text.getText()});
                        Quantity_spinner.setValue(0);
                        Medicine_combobox.setSelectedIndex(0);
                        Dosage_text.setText("");
                    }
                }
            }
        }

    }//GEN-LAST:event_Add_buttonActionPerformed

