    private void Finish_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Finish_buttonActionPerformed
        // TODO add your handling code here:
        int rows = Medicine_dosage_table.getRowCount();
        if (!Disease_text.getText().isEmpty() && rows > 0) {
            String disease = Disease_text.getText();
            String patient_name = Patient_name_label.getText();
            String userid = User_id_label.getText();
            String notes = Notes_text.getText();
            String medicines = "";
            String quantity = "";
            String dosage = "";
            
            for (int i = 0; i < rows; i++) {
                if (i + 1 < rows) {
                    medicines += Medicine_dosage_table.getValueAt(i, 0) + ",";
                    quantity += Medicine_dosage_table.getValueAt(i, 1) + ",";
                    dosage += Medicine_dosage_table.getValueAt(i, 2) + ",";
                } else {
                    medicines += Medicine_dosage_table.getValueAt(i, 0);
                    quantity += Medicine_dosage_table.getValueAt(i, 1);
                    dosage += Medicine_dosage_table.getValueAt(i, 2);
                }
            }
            
            try {
                Date date = new SimpleDateFormat("dd/MM/yyyy").parse(Date_label.getText());           
                ConsultationHistory history = new ConsultationHistory(date, disease, doctor_name, patient_name, userid, medicines, quantity, dosage, notes, 0, false);
                new FinishConsultation().AddHistory(history);
                
                Consultation_panel.setVisible(false);
                Appointment_panel.setVisible(true);
            } catch (NoSuchAlgorithmException | IOException | ParseException ex) {
                Logger.getLogger(ConsultationScreen.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_Finish_buttonActionPerformed

